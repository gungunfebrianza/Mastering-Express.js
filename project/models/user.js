const mongoose = require("mongoose");
const schema = mongoose.Schema;

//create schema
const UserSchema = new schema({
  name: {
    type: String,
    required: true
  },
  email: {
    type: String,
    required: true
  },
  password: {
    type: String,
    required: true
  },
  avatar: {
    type: String
  },
  date: {
    type: Date,
    default: Date.now
  }
});

module.exports = ModelUser = mongoose.model("users", UserSchema);
